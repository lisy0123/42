#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*c;
	int		i;

	c = (char *)malloc(sizeof(*src));
	i = 0;
	while (src[i] != '\0')
	{
		c[i] = src[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
