#include <unistd.h>

char	*ft_strrev(char *str)
{
	char *a;
	char *b;
	char *c;
	char d;

	a = str;
	b = str;
	c = str;
	while (*a != '\0')
	{
		a++;
	}
	a--;
	while (b < a)
	{
		d = *b;
		*b = *a;
		*a = d;
		b++;
		a--;
	}
	return (c);
}
