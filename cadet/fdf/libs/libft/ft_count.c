#include "libft.h"

int			ft_count(char const *s, char c)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s[i])
	{
		if (s[i] == c)
		{
			while (s[i] == c)
				i++;
		}
		else if (s[i])
		{
			len++;
			while (s[i] && s[i] != c)
				i++;
		}
	}
	return (len);
}
